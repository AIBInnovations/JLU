export const getUniqueCategories = () => {
  const categories = Array.from(new Set(courseFees.map(course => course.degree)));
  return categories;
};

export const getUniqueFaculties = () => {
  const faculties = Array.from(new Set(courseFees.map(course => course.faculty)));
  return faculties;
};

export const searchCourses = (query: string) => {
  if (!query.trim()) return courseFees;

  const lowercaseQuery = query.toLowerCase();
  return courseFees.filter(course =>
    course.name.toLowerCase().includes(lowercaseQuery) ||
    course.school.toLowerCase().includes(lowercaseQuery) ||
    course.faculty.toLowerCase().includes(lowercaseQuery)
  );
};

export const filterByCategory = (category: string) => {
  if (!category || category === 'all') return courseFees;
  return courseFees.filter(course => course.degree === category);
};
